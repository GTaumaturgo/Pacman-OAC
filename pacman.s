	sw   $ra, 0($sp)
	
	li   $t1, 30
	li   $t3, 40
	li   $t5, 8
	li   $t7  8
	lw   $a0, bitmap
	la   $a1, simplesbin
	
	jal desenha1
	
	
		
	lw $ra, 0($sp)
	addi $sp, $sp, 4
	jr $ra	

desenha1:
	addi $sp, $sp, -4
	sw   $ra, 0($sp)
	li   $t0, 0
loop_desenha1:	
	beq $t0, $t1, fim_loop_desenha1
	jal desenha2
	
	addi $t0, $t0, 1
	j loop_desenha1
fim_loop_desenha1:
	lw $ra, 0($sp)
	addi $sp, $sp, 4
	jr $ra

desenha2:
	addi $sp, $sp, -4
	sw $ra, 0($sp)
	li   $t2, 0
loop_desenha2:	
	beq $t2, $t3, fim_loop_desenha2
	lb $t8, 0($a1) # t8 = caractere da textura
	# e possiv/el otimizar trocando os beqs por bnes e inserindo vários labels
	
	
	lb $t9, charW
	la $a2, tex0
	beq $t8, $t9, apos_escolher
	
	lb $t9, charP
	la $a2, tex13
	beq $t8, $t9, seta_pos_pacman
	
	lb $t9, charA
	la $a2, tex14
	beq $t8, $t9, seta_pos_azul
	
	lb $t9, charR
	la $a2, tex15
	beq $t8, $t9, seta_pos_rosa
	
	lb $t9, charL
	la $a2, tex16
	beq $t8, $t9, seta_pos_laranja
	
	lb $t9, charV
	la $a2, tex17
	beq $t8, $t9, seta_pos_vermelho

	lb $t9, charF
	la $a2, tex7
	beq $t8, $t9, apos_escolher
	
	lb $t9, charB
	la $a2, tex8
	beq $t8, $t9, apos_escolher

	j erro_reconhecimento_textura
	
apos_escolher:	
	jal desenha3

	addi $t2, $t2, 1
	addi $a1, $a1, 1 # atualiza buftex
	j loop_desenha2
fim_loop_desenha2:
	addi $a0, $a0, 2240 # 320 * 7
	lw $ra, 0($sp)
	addi $sp, $sp, 4
	jr $ra

seta_pos_pacman:
	sb $t0, pacmanY	
	sb $t2, pacmanX
	j apos_escolher
seta_pos_azul:
	sb $t0, azulY
	sb $t2, azulX
	jal debug
	j apos_escolher
seta_pos_rosa:
	sb $t0, rosaY
	sb $t2, rosaX
	j apos_escolher
seta_pos_laranja:
	sb $t0, laranjaY
	sb $t2, laranjaX
	j apos_escolher
seta_pos_vermelho:
	sb $t0, vermelhoY
	sb $t2, vermelhoX
	j apos_escolher
	

desenha3:
	addi $sp, $sp, -4
	sw   $ra, 0($sp)
	li   $t4, 0
loop_desenha3:

	beq $t4, $t5, fim_loop_desenha3 # se t4 == t5, break
	jal desenha4	
	addi $t4, $t4, 1
	j loop_desenha3
fim_loop_desenha3:
	addi $a0, $a0, -2552 # -(320 * 8 - 8)
	lw $ra, 0($sp)
	addi $sp, $sp, 4
	jr $ra

desenha4:
	addi $sp, $sp, -4
	sw   $ra, 0($sp)
	li   $t6, 0
loop_desenha4:
	beq $t6, $t7, fim_loop_desenha4 # se t6 == t7, break
	lb   $a3, 0($a2)
	sb   $a3, 0($a0)
	addi $a2, $a2, 1 # atualiza ponteiro para o buffer
	addi $t6, $t6, 1 # atualiza contador
	addi $a0, $a0, 1 # atualiza bitmap
	j loop_desenha4
fim_loop_desenha4:
	addi $a0, $a0, 312 # 320 - 8
	lw $ra, 0($sp)
	addi $sp, $sp, 4
	jr $ra	


	
# funcao que imprime caso passe por lá, só serve para debug mesmo.
debug:
	addi $sp, $sp, -4
	sw   $a0, 0($sp)
	la $a0, estou_aq
	li $v0, 4
	syscall
	lw  $a0, 0($sp)
	addi $sp, $sp, 4
	jr $ra
	
erro_reconhecimento_textura:
	la $a0, erro_text
	li $v0, 4
	syscall
	move $a0, $t8
	li $v0, 11
	syscall
	li $v0, 10
	syscall
		    
# esse procedimento recebe:
# nome do arquivo em $a0
# buffer  para escrever o que ler em $a1
# tamanho do arquivo em $a2

return: jr $ra
